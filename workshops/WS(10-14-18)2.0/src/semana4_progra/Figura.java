package semana4_progra;

//Esta es la clase de tipo abstracto en donde se encuentran los métodos que se van a utilizar en el resto de las clases.
public abstract class Figura {
    
    //Aquí se crearon dos métodos vacios los cuales llenaremos en las otras clases, ambos métodos se utilizaran en el resto de las clases
    public abstract double calcular();
    public abstract String elemento();
}
